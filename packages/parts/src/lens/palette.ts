/** The three band tints from LENS.EX0 (the RGB triplets after the wid/hig header). */
const BAND_TINTS: ReadonlyArray<readonly [number, number, number]> = [
  [0, 5, 15],
  [0, 7, 26],
  [0, 9, 37],
];

/**
 * Build the 768-byte lens palette: band 0 is the picture palette; bands 1..3 are band 0 tinted by the
 * three LENS.EX0 colours (each component clamped to 63), reproducing MAIN.C:308-325 verbatim.
 */
export function buildLensPalette(): Uint8Array {
  const pal = new Uint8Array(768);
  for (let i = 0; i < 768; i++) pal[i] = BASE_PALETTE[i] ?? 0;
  for (let band = 1; band < 4; band++) {
    const tint = BAND_TINTS[band - 1] ?? [0, 0, 0];
    for (let a = 0; a < 64 * 3; a += 3) {
      pal[a + band * 192] = Math.min(63, tint[0] + (pal[a] ?? 0));
      pal[a + band * 192 + 1] = Math.min(63, tint[1] + (pal[a + 1] ?? 0));
      pal[a + band * 192 + 2] = Math.min(63, tint[2] + (pal[a + 2] ?? 0));
    }
  }
  return pal;
}
