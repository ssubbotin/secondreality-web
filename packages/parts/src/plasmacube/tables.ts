/**
 * INITVECT (PLZFILL.C:28-32): sini[a] = trunc(sin(a/1024·π·4)·127), a ∈ [0,1524). C double→int
 * truncates toward zero. (The original also runs `s -= sini[a]` into a dead local — no effect.)
 */
export function buildSini(): Int16Array {
  const t = new Int16Array(1524);
  for (let a = 0; a < 1524; a++) t[a] = Math.trunc(Math.sin((a / 1024) * Math.PI * 4) * 127);
  return t;
}
