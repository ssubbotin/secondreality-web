/** Build the full RATA control-point list (36 explicit + 100 tail = 136 points). */
export function buildRata(): RataPoint[] {
  const out: RataPoint[] = [...RATA_EXPLICIT];
  for (let i = 0; i < 100; i++) out.push(RATA_TAIL);
  return out;
}

/** Number of control points in the spline path. */
