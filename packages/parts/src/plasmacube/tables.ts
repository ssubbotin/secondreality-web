/**
 * Verbatim data tables for the PLZPART cube (`vect()`), regenerated from the original sources and
 * verified byte-for-byte against the shipped data where it exists (see tables.test.ts):
 *   - sinit/kosinit  → SINIT.INC (the 16-bit rotation sine), asserted against __fixtures__/SINIT.DAT.
 *   - splinecoef     → SPLINE.INC (the cubic-spline basis, 1024 words).
 *   - rata (buu)     → RATA.INC  (the camera-path control points).
 *   - sini           → INITVECT (PLZFILL.C initvect, the cube-tile sine).
 */

export const TWO_PI = Math.PI * 2;

/**
 * SINIT.INC: a 16-bit sine, period 1024, amplitude 32766. The shipped table rounds the positive half
 * half-up (`floor(x+0.5)`) but the negative half is one larger (the generator biased the magnitude),
 * so `sin < 0` carries a `+1`. With θ taken modulo 1024 this is byte-exact for indices 0..1279.
 * `kosinit[i] = sinit[i + 256]` (the original `kosinit = sinit + 512` bytes = +256 words).
 */
export function buildSinit(): Int16Array {
  const t = new Int16Array(1280);
  for (let i = 0; i < 1280; i++) {
    const s = Math.sin(((i % 1024) * TWO_PI) / 1024);
    const v = Math.floor(32766 * s + 0.5);
    t[i] = s < 0 ? v + 1 : v;
  }
  return t;
}

/** kosinit[i] = sinit[i + 256] (quarter-phase shift), i ∈ [0,1024). */
export function buildKosinit(sinit: Int16Array): Int16Array {
  const t = new Int16Array(1024);
  for (let i = 0; i < 1024; i++) t[i] = sinit[i + 256] ?? 0;
  return t;
}

